    public void recordsTable() {
        // Days work table
        DefaultTableModel table = (DefaultTableModel) jTablePayrollRecords.getModel();
        table.setRowCount(0);
        // Get the employee list
        ArrayList<Records> recordList = new Read(Data.RECORDS).recordList();
        Collections.sort(recordList, new SortRecords().reversed());

        for (Records r : recordList) {
            Vector v = new Vector();
            v.add(r.getId());
            v.add(r.getEmployeeId());
            v.add(r.getEmployeName());
            v.add(r.getDateFrom());
            v.add(r.getDateTo());
            v.add(r.getSalary());
            v.add(r.getRateOfPay());
            v.add(r.getOvertimeRate());
            v.add(r.getAllowance());
            v.add(r.getNoOvertimeHours());
            v.add(r.getHoursAbsence());
            table.addRow(v);
        }
    }

