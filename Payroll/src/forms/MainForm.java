    private void jButtonDeletePayrollActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeletePayrollActionPerformed
        try {
            if (selectedEmployeeId != 0 && selectedRow != 0) {
                // Show input confirm dialog to ask the user 
                int result = JOptionPane.showConfirmDialog(this,
                        "Are you sure you want to delete the selected record with row no. "
                        + selectedRow + "?", "Delete", JOptionPane.WARNING_MESSAGE);
                if (result == 0) {
                    // Get the employee list
                    ArrayList<Records> recordList = new Read(Data.RECORDS).recordList();
                    ArrayList<Records> newRecordList = new ArrayList<>();
                    // Selected record id
                    int selectedRecordId = 0;
                    for(Records r: recordList) {
                        if(selectedRow != r.getId()) {
                            Records rec = new Records();
                            rec.setId(r.getId());
                            rec.setEmployeeId(r.getEmployeeId());
                            rec.setEmployeName(r.getEmployeName());
                            rec.setDateFrom(r.getDateFrom());
                            rec.setDateTo(r.getDateTo());
                            rec.setSalary(r.getSalary());
                            rec.setRateOfPay(r.getRateOfPay());
                            rec.setOvertimeRate(r.getOvertimeRate());
                            rec.setAllowance(r.getAllowance());
                            rec.setNoOvertimeHours(r.getNoOvertimeHours());
                            rec.setHoursAbsence(r.getHoursAbsence());
                            newRecordList.add(rec);
                        } else {
                            selectedRecordId = r.getId();
                        }
                    }
                    // New Records list
                    Writer w = new Writer(Data.RECORDS);
                    w.addRecords(newRecordList);
                    
                    // Clear the deleted payroll details
                    // Insert the payroll new list
                    ArrayList<Payroll> payrollList = new Read(Data.PAYROLL).payrollList();
                    ArrayList<Payroll> newPayrollList = new ArrayList<>();
                    for(Payroll p: payrollList) {
                        if(selectedRecordId != p.getRecordsId()) {
                            Payroll payroll = new Payroll();
                            payroll.setId(p.getId());
                            payroll.setEmployeeId(p.getEmployeeId());
                            payroll.setRecordsId(p.getRecordsId());
                            payroll.setDate(p.getDate());
                            payroll.setDay(p.getDay());
                            payroll.setHours(p.getHours());
                            payroll.setOvertime(p.getOvertime());
                            newPayrollList.add(payroll);
                        }
                    }
                    // Write the new payroll data
                    Writer wNewPayroll = new Writer(Data.PAYROLL);
                    wNewPayroll.addPayroll(newPayrollList);
                    
                    // Load the table
                    //recordsTable();
                    selectedRow = 0;
                    selectedEmployeeId = 0;
                } else {
                    selectedRow = 0;
                    selectedEmployeeId = 0;
                }
                // Load the table
                recordsTable();
                jLabelSelectedRow.setText("...");
            } else {
                JOptionPane.showMessageDialog(this, "Double click the table row to delete a record",
                        "Info", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            System.out.println("jButtonDeletePayrollActionPerformed() -> " + e.getMessage());
        }
    }//GEN-LAST:event_jButtonDeletePayrollActionPerformed

