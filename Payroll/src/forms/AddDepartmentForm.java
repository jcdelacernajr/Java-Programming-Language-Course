    public void departmentTable() {
        // Position table
        DefaultTableModel table = (DefaultTableModel) jTableDepartment.getModel();
        // Set the initial table row count
        table.setRowCount(0);
        // Get first the existing data
        ArrayList<Department> departmentList = new Read(Data.DEPARTMENT).departmentList();
        // Sort
        Collections.sort(departmentList, new SortDepartment().reversed());
        for (Department department : departmentList) {
            // Vector array
            Vector v = new Vector();
            v.add(department.getId());
            v.add(department.getDepartment());
            // Add the vector array object
            table.addRow(v);
        }
    }

