    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        if (jTextFieldDepartment.getText().length() > 0) {
            // Get first the existing data
            ArrayList<Department> departmentList = new Read(Data.DEPARTMENT).departmentList();
            // Sort the list first
            Collections.sort(departmentList, new SortDepartment().reversed());

            // Create a new department instance 
            Department department = new Department();
            // If the departmentList is empty, set the rowId to 1
            // If departmentList is not empty get the value in index zero to add 1 
            int rowId = departmentList.isEmpty() ? 1 : departmentList.get(0).getId() + 1;
            department.setId(rowId);
            department.setDepartment(jTextFieldDepartment.getText());
            // Add to departmentList
            departmentList.add(department);

            // Write the text into file
            Writer writer = new Writer(Data.DEPARTMENT);
            writer.addDepartment(departmentList);
            
            // Show a success message
            JOptionPane.showMessageDialog(this, "Department table is successfully "
                    + "updated", "Info", JOptionPane.INFORMATION_MESSAGE);
            
            // Cleat the input field
            jTextFieldDepartment.setText("");
            jTextFieldDepartment.requestFocus();

            // Populate the data again
            departmentTable();
        } else {
            JOptionPane.showMessageDialog(this, "Please a input a Departmen"
                    + " name", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextFieldDepartment.requestFocus();
        }
    }//GEN-LAST:event_jButtonSaveActionPerformed

    private void jTableDepartmentMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableDepartmentMouseClicked
        // Department table
         DefaultTableModel table = (DefaultTableModel) jTableDepartment.getModel();
        // get the selected department id
        selectedDepartmentId = Integer.parseInt(table.getValueAt(
                jTableDepartment.getSelectedRow(), 0).toString());
        
        // Show input dialog to ask the user to upddate the department
        String result = (String)JOptionPane.showInputDialog(this, "Department", "Update",
                JOptionPane.QUESTION_MESSAGE, null, null, 
                table.getValueAt(jTableDepartment.getSelectedRow(), 1).toString());
        
        if(result != null) {
            ArrayList<Department> departmentsList = new Read(Data.DEPARTMENT).departmentList();
            ArrayList<Department> newDepartmentsList = new ArrayList<>();
            for(Department departments: departmentsList) {
                Department d = new Department();
                if(selectedDepartmentId == departments.getId()) {
                    d.setId(selectedDepartmentId);
                    d.setDepartment(result);
                } else {
                    d.setId(departments.getId());
                    d.setDepartment(departments.getDepartment());
                }
                newDepartmentsList.add(d);
            }
            // Write the data again
            Writer w = new Writer(Data.DEPARTMENT);
            w.addDepartment(newDepartmentsList);
            // Load the table again
            departmentTable();
        } 
    }//GEN-LAST:event_jTableDepartmentMouseClicked

