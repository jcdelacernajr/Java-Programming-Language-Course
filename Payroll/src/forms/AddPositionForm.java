    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        if (jTextFieldPosition.getText().length() > 0) {
            // Get first the existing data
            ArrayList<Position> positionsList = new Read(Data.POSITION).positionList();
            // Sort the list first
            Collections.sort(positionsList, new SortPosition().reversed());
            // Create a new position instance 
            Position position = new Position();
            // If the positionsList is empty, set the rowId to 1
            // If positionList is not empty get the value in index zero to add 1 
            int rowId = positionsList.isEmpty() ? 1 : positionsList.get(0).getId() + 1;
            position.setId(rowId);
            position.setDepartmentId(selecetedDepartmentId);
            position.setPosition(jTextFieldPosition.getText());
            // Add to positionList
            positionsList.add(position);
            
            // Write the text into file
            Writer writer = new Writer(Data.POSITION);
            writer.addPosition(positionsList);
            
            // Clear
            jTextFieldPosition.setText("");
            
            // load the position table
            positionTable(selecetedDepartmentId);
        } else {
            JOptionPane.showMessageDialog(this, "Please input a position name", "Warning", JOptionPane.WARNING_MESSAGE);
            jTextFieldPosition.requestFocus();
        }
    }//GEN-LAST:event_jButtonSaveActionPerformed

