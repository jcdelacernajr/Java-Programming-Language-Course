    /**
     * Load position table
     * 
     * @param departmentId 
     */
    public void positionTable(int departmentId) {
        // Position table
        DefaultTableModel table = (DefaultTableModel) jTablePosition.getModel();
        // Set the initial table row count
        table.setRowCount(0);
        // Get first the existing data
        ArrayList<Position> positionsList = new Read(Data.POSITION).positionList();
        // Sort
        Collections.sort(positionsList, new SortPosition().reversed());
        for(Position p: positionsList) {
            if(departmentId == p.getDepartmentId()) {
                Vector v = new Vector();
                v.add(p.getId());
                v.add(p.getPosition());
                table.addRow(v);
            }
        }
        jTablePosition.requestFocus();
    }

