    /**
     * Validate all the user input
     *
     * @return 0 to input again and 1 for valid input
     */
    public int inputValidation() {
        String date = dateFieldValidation("Date", jTextFieldDate);
        if (date.isEmpty()) {
            return 0;
        }
        
        String day = numberFieldValidation("Day", jTextFieldDay);
        if (day.isEmpty()) {
            return 0;
        }

        String hours = workingHoursValidation("Hours", employeeType(selectedEmployeeId), jTextFieldHours);
        if (hours.isEmpty()) {
            return 0;
        }

        String overtime = oTValidation("Overtime", "Non - Teaching", jTextFieldOvertime);
        if (overtime.isEmpty()) {
            return 0;
        }

        return 1;
    }
    
    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed
        try {
            if (inputValidation() == 1) {
                // Get the data gain
                ArrayList<EditWorkingDays> workingDaysList = new Read(Data.EDIT_WORKING_DAYS).editWorkingDaysList();
                ArrayList<EditWorkingDays> newWorkingDaysList = new ArrayList<>();
                for (EditWorkingDays e : workingDaysList) {
                    EditWorkingDays newDay = new EditWorkingDays();
                    if (selectedRecordId == e.getRecordId() && selectedTableRow == e.getId()) {
                        // System.out.println("selectedTableRow: "+ selectedTableRow+ " selectedRecordId: " + selectedRecordId);
                        newDay.setId(e.getId());
                        newDay.setRecordId(e.getRecordId());
                        newDay.setEmployeeId(selectedEmployeeId);
                        newDay.setDate(jTextFieldDate.getText());
                        newDay.setDay(Integer.parseInt(jTextFieldDay.getText()));
                        newDay.setHours(Integer.parseInt(jTextFieldHours.getText()));
                        newDay.setOvertime(Integer.parseInt(jTextFieldOvertime.getText()));
                    } else {
                        newDay.setId(e.getId());
                        newDay.setRecordId(e.getRecordId());
                        newDay.setEmployeeId(selectedEmployeeId);
                        newDay.setDate(e.getDate());
                        newDay.setDay(e.getDay());
                        newDay.setHours(e.getHours());
                        newDay.setOvertime(e.getOvertime());
                    }
                    newWorkingDaysList.add(newDay);
                }
                // Write the data again
                Writer w = new Writer(Data.EDIT_WORKING_DAYS);
                w.addEditWorkingsDay(newWorkingDaysList);
                this.dispose();
            }
        } catch (Exception e) {
            System.out.println("jButtonUpdateActionPerformed() -> " + e.getMessage());
        }
    }//GEN-LAST:event_jButtonUpdateActionPerformed

