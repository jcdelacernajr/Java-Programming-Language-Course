    public void setTextField() {
        // Get the data gain
        ArrayList<EditWorkingDays> workingDaysList = new Read(Data.EDIT_WORKING_DAYS).editWorkingDaysList();
        for (EditWorkingDays e : workingDaysList) {
            if (selectedRecordId == e.getRecordId() && selectedTableRow == e.getId()) {
                jTextFieldDate.setText(String.valueOf(e.getDate()));
                jTextFieldDay.setText(String.valueOf(e.getDay()));
                jTextFieldDay.setEditable(false);
                jTextFieldHours.setText(String.valueOf(e.getHours()));
                jTextFieldOvertime.setText(String.valueOf(e.getOvertime()));
            }
        }

        // Disabled the overtime for wmployee type Teaching
        if (employeeType(selectedEmployeeId).equals("Teaching")) {
            jTextFieldOvertime.setEnabled(false);
        }
    }

